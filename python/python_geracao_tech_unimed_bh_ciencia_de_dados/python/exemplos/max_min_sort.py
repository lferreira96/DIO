numeros = [100,2,5,10,500,1000,24,35,21]

print(numeros)
# Max - Retorna o máximo entre os números
# Min - Retorna o mínimo entre os números
print(max(numeros),min(numeros))


# A função sorted - exibe ordenado sem ordenar efetivamente, mantendo as posições originais
print(sorted(numeros))
print(numeros)

# Sort - Organiza os números em ordem crescente
numeros.sort()
print(numeros)
